public class P1_Array {
    public static void main(String[] args) {
        String  [] num = {"Shaik" , "Mohammed" , "Ibrahim"};
        int [] num1 = {0 , 1 , 2 , 3 , 4};
        for(int i = 0 ; i <= num.length-1 ; i++)
        {
            System.out.println(num[i]);
        }
        for(int i = 0 ; i <= num1.length-1 ; i++)
        {
            System.out.println(num1[i]);
        }
    }
}