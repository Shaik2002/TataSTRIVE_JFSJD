//Arithmetic Operators - Addition(+), Subtraction(-), Division(/),
//Multiplication(*), Modulus(%), Increment(++), Decrement(--)
let a = 50
let b = 20
console.log(a+b)
console.log(a-b)
console.log(a*b)
console.log(a/b)
console.log(a%b)
console.log(++a)
console.log(--b)

//Relational Operators - Lesser Than(<), Greater Than(>), Equal to(=),
//Not Equal to(!=), Identical(===)
let c = 10
let d = 20
console.log(c<d)
console.log(c>d)
console.log(c!=d)
console.log(c<=d)
console.log(c>=d)
console.log(c==d)
console.log(c===d)

//Logical Operators - AND(&&), OR(||), NOT(!)
var e = 1
var f = 0
console.log(e && f)
console.log(e || f)
console.log(!f)

//Bitwise Operators - AND(&), OR(|), XOR(^), NOT(~), RightShift(>>), LeftShift(<<)
var g = 10
var h = 20
console.log(g & h)
console.log(g | h)
console.log(g ^ h)
console.log(~ h)
console.log(g >> 2)
console.log(h << 3)